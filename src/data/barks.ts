// Chaos mode combines two random categories
export function getChaosBark(): string {
  const categoryIds = CATEGORIES.map(c => c.id);
  const cat1 = categoryIds[Math.floor(Math.random() * categoryIds.length)];
  let cat2 = categoryIds[Math.floor(Math.random() * categoryIds.length)];
  while (cat1 === cat2) {
    cat2 = categoryIds[Math.floor(Math.random() * categoryIds.length)];
  }
  
  const bark1 = BARKS[cat1][Math.floor(Math.random() * BARKS[cat1].length)];
  const bark2 = BARKS[cat2][Math.floor(Math.random() * BARKS[cat2].length)];
  
  // Truncate and combine
  const part1 = bark1.split('.')[0] + '.';
  const part2 = bark2.split('.')[0].toLowerCase();
  return `${part1} Also, ${part2}.`;
}

// Get a random bark, with 1% chance of rare
export function getRandomBark(category?: string): { text: string; category: string; isRare: boolean } {
  // 1% chance for rare bark
  if (Math.random() < 0.01) {
    return {
      text: RARE_BARKS[Math.floor(Math.random() * RARE_BARKS.length)],
      category: 'rare',
      isRare: true,
    };
  }
  
  const cat = category || CATEGORIES[Math.floor(Math.random() * CATEGORIES.length)].id;
  const barks = BARKS[cat] || BARKS.soft;
  
  return {
    text: barks[Math.floor(Math.random() * barks.length)],
    category: cat,
    isRare: false,
  };
}

